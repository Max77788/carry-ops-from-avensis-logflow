// Export sorted carriers
export const CARRIERS = CARRIER_VEHICLES_MAP.map((item) => item.Carrier).sort(
  (a, b) =>
    a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" })
);

// Get all unique vehicles
const ALL_VEHICLES = CARRIER_VEHICLES_MAP.flatMap((item) => item.Vehicles);
export const TRUCKS_SORTED = [...new Set(ALL_VEHICLES)].sort((a, b) =>
  a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" })
);

// Helper function to filter trucks by carrier
export const getTrucksByCarrier = (carrierName: string): string[] => {
  if (!carrierName) return TRUCKS_SORTED;

  const carrierData = CARRIER_VEHICLES_MAP.find(
    (item) => item.Carrier.toLowerCase() === carrierName.toLowerCase()
  );

  if (!carrierData) return [];

  return carrierData.Vehicles.sort((a, b) =>
    a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" })
  );
};

// Helper function to search trucks
export const searchTrucks = (query: string): string[] => {
  if (!query) return TRUCKS_SORTED;

  const lowerQuery = query.toLowerCase();
  return TRUCKS_SORTED.filter((truck) =>
    truck.toLowerCase().includes(lowerQuery)
  );
};

// Helper function to search carriers
export const searchCarriers = (query: string): string[] => {
  if (!query) return CARRIERS;

  const lowerQuery = query.toLowerCase();
  return CARRIERS.filter((carrier) =>
    carrier.toLowerCase().includes(lowerQuery)
  );
};

